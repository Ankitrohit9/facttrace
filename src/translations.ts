// Map original category database values to key
export function getCategoryKey(cat: string): string {
  if (cat.includes("Pothole")) return "pothole";
  if (cat.includes("Garbage")) return "garbage";
  if (cat.includes("Streetlight")) return "streetlight";
  if (cat.includes("Water") || cat.includes("Leak")) return "leakage";
  if (cat.includes("Parking")) return "parking";
  if (cat.includes("Traffic")) return "traffic";
  if (cat.includes("Vandalism") || cat.includes("Public Facility")) return "vandalism";
  return "general";
}

// Map original status database values to key
export function getStatusKey(status: string): string {
  const norm = status.toLowerCase();
  if (norm === "reported") return "reported";
  if (norm === "processing") return "processing";
  if (norm === "in_progress") return "in_progress";
  if (norm === "community resolved") return "community resolved";
  if (norm === "ai recommended review") return "ai recommended review";
  if (norm === "duplicate") return "duplicate";
  return norm;
}

export function translate(key: string, lang: Language): string {
  const dict = translations[lang] || translations.en;
  return dict[key] || translations.en[key] || key;
}
